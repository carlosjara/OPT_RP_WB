AND TRUNC(FECHA) IN (SELECT TRUNC(FEC_HOMLGD_MAYOR) FROM I_BMIA.APX_REPONLINE_PARAM WHERE TRUNC(FEC_ACT) = TRUNC(CURRENT_DATE));